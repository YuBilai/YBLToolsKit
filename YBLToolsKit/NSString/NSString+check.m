+ (BOOL)luhnCheckBankCardNum:(NSString *)cardNum {
    long long n = [cardNum longLongValue];
    NSInteger sum = 0;
    while (n > 0) {
        NSInteger a = n % 10;
        n = n / 10;
        
        NSInteger b = (n % 10) * 2;
        n = n / 10;
        
        if (b > 9) {
            b -= 9;
        }
        
        sum += a + b;
    }
    
    return sum % 10 == 0;
}

